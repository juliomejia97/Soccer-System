    public SeleccionarSilla(Integer idPartido, String nombreEstadio, String categoria, long  cant, String fechaHora,String ciudad, String equipo) {
        initComponents();
        Conexion con = new Conexion();
        this.idPartido=idPartido;
        this.nombreEstadio=nombreEstadio;
        this.categoria=categoria;
        this.cant=cant;
        this.fechaHora=fechaHora;
        this.ciudad=ciudad;
        this.equipo=equipo;
        this.jTextField1.setText(nombreEstadio);
        this.jTextField4.setText(String.valueOf(idPartido));
        this.jTextField2.setText(categoria);
        this.jTextField3.setText(String.valueOf(cant));
        CategoriaJpaController controC = new CategoriaJpaController(con.getEmf());
        Integer idC = controC.getIdCategoriaN(categoria);
        SillaxpartidoJpaController controSp = new SillaxpartidoJpaController((con.getEmf()));
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        List<Integer> resul = controSp.mostrarSillasXPartidoC(idPartido, nombreEstadio, idC);
        for(Integer row:resul)
        {     
            Integer i; 
            i = row;
            model.addRow(new Object[]{row,false});
        }
        
    }

