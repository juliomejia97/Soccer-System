    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        AnotacionJpaController controA = new AnotacionJpaController(this.con.getEmf());
        Integer idA = controA.getAnotacionCount()+1;
        Short min = Short.parseShort(jTextField1.getText());
        Short tiempo = Short.parseShort((String) jComboBox3.getSelectedItem());
        String tipoGol = (String) jComboBox5.getSelectedItem();
        String var = (String) jComboBox4.getSelectedItem();
        Integer idPa = Integer.parseInt(jTextField5.getText());
        String id= (String) jComboBox2.getSelectedItem();
        Scanner tokenize = new Scanner(id);
        Integer idJugador =tokenize.nextInt();
        System.out.println(idJugador);
        Integer idAno=controA.getAnotacionCount()+1;
        try {
            controA.agregarAnotacion(idAno ,idJugador, min, tiempo, tipoGol, var, idPartido, idJugador);
        } catch (Exception ex) {
            Logger.getLogger(AgregarAnotacion.class.getName()).log(Level.SEVERE, null, ex);
        }
        dispose();
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                new DetallesPartido(idPartido).setVisible(true);
            }
        });
    }//GEN-LAST:event_jButton3MouseClicked

